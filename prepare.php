              <h2>Eat</h2>
              <p style="font-family:sans-serif;font-size: 20px">Eating regularly before donating will help to keep your blood sugar levels stable. This is important so that you don’t feel lightheaded or dizzy after your donation. Having a snack before donation can help maintain these blood sugar levels. Ensuring that your diet contains foods rich in iron - such as meats and green leafy vegetables - will help to keep you feeling well during and after donation.</p>
              <h2>Drink</h2>
               <p style="font-family:sans-serif;font-size: 20px">Almost half of the blood that you donate is made up of water. The fluids that you lose during donation can cause a drop in blood pressure – causing you to feel faint and dizzy.</p>
                <p style="font-family:sans-serif;font-size: 20px">To help prevent this from happening we ask you to drink 500ml of water immediately before you give blood - we’ll give this to you before you donate. Please help us reduce our plastic usage by bringing your own refillable bottle.</p>
                 <p style="font-family:sans-serif;font-size: 20px">It’s also important to ensure that you are well hydrated in the days leading up to your donation. This will help to compensate for the fluids lost during donation, and will help to bring your blood volume levels back to normal.
It is essential to avoid alcohol before and after donating as this may affect hydration levels and delay recovery.</p>
                  <h2>Exercise</h2>
                  <p style="font-family:sans-serif;font-size: 20px">Avoid doing any vigorous exercise or heavy lifting the day of your donation – both before and after you’ve given blood.</p>
                   <p style="font-family:sans-serif;font-size: 20px">Keeping your body in a rested state is important to give it a chance to replenish the fluids lost during donation, which will help you avoid feeling dizzy or lightheaded and keep you well. Light exercise such as walking is fine, but please make sure that you are fully recovered and hydrated prior to your donation.</p>
                   <h2>Wear</h2>
                    <p style="font-family:sans-serif;font-size: 20px">We will need to roll up your sleeve past your elbow when you give blood – to allow easy access to your veins. To make sure that you are comfortable, please wear clothing with loose sleeves.</p>
                    <h2>Distract</h2>
                     <p style="font-family:sans-serif;font-size: 20px">It is normal to feel nervous when you come along to donate – particularly if this is your first time. Our friendly and helpful staff will put you at ease straightaway. 
Distraction is key. You may want to bring a friend to wait with you, or feel free to bring a book to read or listen to some music while you donate.</p>
                      <p style="font-family:sans-serif;font-size: 20px">We also ask donors to undertake something called ‘applied muscle tension’ or AMT. This is a simple behavioural technique that helps to maintain blood pressure, and so stops you from feeling faint or unwell. It is also a brilliant distraction technique! AMT involves tensing and relaxing the body’s major muscles during the donation process. A favourite of ours is clenching and unclenching your buttock muscles (no joke!).</p>
                       <p style="font-family:sans-serif;font-size: 20px">Further information about this will be available at your donation, or simply ask a member of staff who will be happy to advise you further.</p>
                       <h2>Know</h2>
                       <p style="font-family:sans-serif;font-size: 20px">Once you’ve made the decision to donate and made the effort to go along to your appointment, you want to be sure that you can actually give blood.</p>
                       <p style="font-family:sans-serif;font-size: 20px">Although most people can give blood, there are some restrictions - depending on things like your health, medication, and whether you've been abroad recently.</p>
                       <p style="font-family:sans-serif;font-size: 20px">Check you can give blood using our interactive questions which cover the most common reasons donors are unable to donate when they attend their appointment. </p>
                       <p style="font-family:sans-serif;font-size: 20px">If you have any specific queries about your eligibility to give blood you can search our health and eligibility or travel sections for advice or call us on 0300 123 23 23.</p>
                       <h2>Sleep</h2>
                       <p style="font-family:sans-serif;font-size: 20px">We recommend that you have a full night’s sleep of between 7 to 9 hours the night before your donation. This will help you to feel more alert when you give blood, which will in turn reduce the risk of feeling unwell.</p>

